'''The following code keeps looping forever. (You can hit Ctrl-C to stop it.) Why does the loop keep running? Modify it so that it stops after the first iteration.

while True:
    print("and on")'''

while True:
    print("and on")
    break

# the simplest way to terminate the loop is to use the 'break' statemnent, break statement can be used with any type of loop 